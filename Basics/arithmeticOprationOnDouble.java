package Basics;
public class arithmeticOprationOnDouble {
    public static void main(String[] args) {
        double x = 5, y = 2;
        System.out.println(x+y); // 7.0
        System.out.println(x-y); // 3.0
        System.out.println(x*y); // 10.0
        System.out.println(x/y); //2.5
    }
}
